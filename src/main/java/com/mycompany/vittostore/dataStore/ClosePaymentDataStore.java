package com.mycompany.vittostore.dataStore;

public class ClosePaymentDataStore {
    private double efectivo, cuentaCorriente, debito, credito , mercadoPago , total;
    
    public double getEfectivo() {
        return efectivo;
    }

    public void setEfectivo(double efectivo) {
        this.efectivo = efectivo;
    }

    public double getCuentaCorriente() {
        return cuentaCorriente;
    }

    public void setCuentaCorriente(double cuentaCorriente) {
        this.cuentaCorriente = cuentaCorriente;
    }

    public double getDebito() {
        return debito;
    }

    public void setDebito(double debito) {
        this.debito = debito;
    }

    public double getCredito() {
        return credito;
    }

    public void setCredito(double credito) {
        this.credito = credito;
    }

    public double getMercadoPago() {
        return mercadoPago;
    }

    public void setMercadoPago(double mercadoPago) {
        this.mercadoPago = mercadoPago;
    }

    public double getTotal() {
        return total;
    }

    public void setTotal(double total) {
        this.total = total;
    }
    
    
}
